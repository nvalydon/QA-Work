package Arrays;

public class Arrays {

	public static void main(String[] args) {
	
		
	
		
		
	int [] numArray = new int[10];
	
	
	for(int i = 0; i < numArray.length; i++) {
	
		numArray[i] = i+1; 
		
		
	}
	
	for(int j = 0; j < numArray.length; j++) {
		
		System.out.print("\t" + numArray[j]);
		System.out.println("\t" + numArray[j]*10);
		
		
	}
	
		
		
	}
	
	
	
	
	
	 
	
	
	
	
}
